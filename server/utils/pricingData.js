// Normalize input for lookup
export function normalizePricingKey(make, model) {
  const makeNorm = make.toLowerCase().trim();
  const modelNorm = model
    .toLowerCase()
    .trim()
    .replace(/\s+/g, ' ')
    .replace(/[®™]/g, '');
  return `${makeNorm}|${modelNorm}`;
}

// Get base MSRP with confidence level
export function getBasePrice(make, model) {
  const key = normalizePricingKey(make, model);

  // Exact match
  if (MODEL_BASE_MSRP[key]) {
    return {
      msrp: MODEL_BASE_MSRP[key],
      confidence: 'verified'
    };
  }

  // Try partial match - find first key where make matches and model substring matches
  const makeNorm = make.toLowerCase().trim();
  const modelNorm = model.toLowerCase().trim().replace(/\s+/g, ' ');

  for (const dbKey in MODEL_BASE_MSRP) {
    const [dbMake, dbModel] = dbKey.split('|');
    if (dbMake === makeNorm) {
      // Check if significant parts of the model name match
      const modelParts = modelNorm.split(' ').filter(p => p.length > 1);
      const matchCount = modelParts.filter(part => dbModel.includes(part)).length;

      if (matchCount > 0 && matchCount === modelParts.length) {
        // Strong partial match
        return {
          msrp: MODEL_BASE_MSRP[dbKey],
          confidence: 'estimated'
        };
      }
    }
  }

  // Brand floor fallback
  const brandFloor = BRAND_FLOOR_MSRP[makeNorm];
  if (brandFloor) {
    return {
      msrp: brandFloor,
      confidence: 'estimated'
    };
  }

  // Ultimate fallback
  return {
    msrp: 35000,
    confidence: 'unknown'
  };
}
